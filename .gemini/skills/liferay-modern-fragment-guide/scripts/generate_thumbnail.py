def create_png_with_text(width, height, bg_color, text, output_path):
    canvas = [[list(bg_color) for _ in range(width)] for _ in range(height)]
    text = text.upper()
    scale = 4
    char_spacing = 2 * scale
    
    lines = [text[i:i+9] for i in range(0, len(text), 9)] if len(text) > 9 else [text]
    
    text_color = [255, 255, 255] if sum(bg_color) < 400 else [0, 0, 0]
    
    line_height = 7 * scale
    line_spacing = 3 * scale
    total_height = len(lines) * line_height + max(0, len(lines) - 1) * line_spacing
    start_y = (height - total_height) // 2
    
    for line_idx, line in enumerate(lines):
        line_width = len(line) * (5 * scale) + max(0, len(line) - 1) * char_spacing
        start_x = max(0, (width - line_width) // 2)
        curr_x = start_x
        curr_y = start_y + line_idx * (line_height + line_spacing)
        
        for char in line:
            bitmap = FONT.get(char, FONT.get(' ', [0]*7))
            for row_idx, row in enumerate(bitmap):
                for col_idx in range(8):
                    if (row >> (7 - col_idx)) & 1:
                        for sy in range(scale):
                            for sx in range(scale):
                                py, px = curr_y + row_idx * scale + sy, curr_x + col_idx * scale + sx
                                if 0 <= py < height and 0 <= px < width:
                                    canvas[py][px] = text_color
            curr_x += 5 * scale + char_spacing

    png_header = b'\x89PNG\r\n\x1a\n'
    ihdr_data = struct.pack('>IIBBBBB', width, height, 8, 2, 0, 0, 0)
    ihdr_chunk = make_chunk(b'IHDR', ihdr_data)
    raw_data = b''
    for y in range(height):
        raw_data += b'\x00'
        for x in range(width):
            raw_data += bytes(canvas[y][x])
    idat_data = zlib.compress(raw_data)
    idat_chunk = make_chunk(b'IDAT', idat_data)
    iend_chunk = make_chunk(b'IEND', b'')
    with open(output_path, 'wb') as f:
        f.write(png_header)
        f.write(ihdr_chunk)
        f.write(idat_chunk)
        f.write(iend_chunk)

def make_chunk(type, data):
    length = struct.pack('>I', len(data))
    crc = struct.pack('>I', zlib.crc32(type + data) & 0xFFFFFFFF)
    return length + type + data + crc

if __name__ == "__main__":
    if len(sys.argv) < 3:
        print("Usage: python generate_thumbnail.py <label> <output_path>")
        sys.exit(1)
    label = sys.argv[1]
    output_path = sys.argv[2]
    color = (random.randint(50, 200), random.randint(50, 200), random.randint(50, 200))
    create_png_with_text(320, 160, color, label, output_path)
    print(f"Generated thumbnail for {label} at {output_path}")
